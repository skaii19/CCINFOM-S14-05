	team_id VARCHAR(4),
    CONSTRAINT FOREIGN KEY (team_id) REFERENCES team(team_id),
    tournament_id VARCHAR(7),
    FOREIGN KEY (tournament_id) REFERENCES tournament(tournament_id),
    placement VARCHAR(5),
    winnings DECIMAL(10,2),
    
    PRIMARY KEY(team_id, tournament_id)
);

